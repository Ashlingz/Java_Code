        xInsert i = new xInsert();
        i.InsertSession(total);
        
        String id = import_txtImport_id.getText();

        Connection con = null;
        PreparedStatement statement = null;
        PreparedStatement statement1 = null;
        PreparedStatement statement2 = null;
        PreparedStatement statement3 = null;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory?autoReconnect=true&useSSL=false", "root", "");

            // First, insert into the imports table
            String sql_import = "INSERT INTO imports(total, import_date) SELECT total, now() FROM sessions ORDER BY session_id DESC LIMIT 1";
            statement = (PreparedStatement) con.prepareStatement(sql_import);
            statement.executeUpdate();

            // Then, insert into supplies from carts and update existing supplies quantities
            String sql_supply = "INSERT INTO supplies(product_id, import_id, qty, price, subtotal) SELECT product_id, ?, qty, price, total FROM carts";
            statement1 = (PreparedStatement) con.prepareStatement(sql_supply);
            statement1.setString(1, id);
            statement1.executeUpdate();

            // Increase product quantities in supplies based on product names
            String sql_increase_qty = "UPDATE products p " +
                         "INNER JOIN carts c ON p.product_id = c.product_id " +
                         "SET p.qty = p.qty + c.qty " +
                         "WHERE c.product_id IN (SELECT DISTINCT product_id FROM carts)";
            statement2 = (PreparedStatement) con.prepareStatement(sql_increase_qty);
            statement2.executeUpdate();

            // Clear the carts and sessions tables
            String sql_cart = "DELETE FROM carts;";
            statement3 = (PreparedStatement) con.prepareStatement(sql_cart);
            statement3.executeUpdate();

            String sql_session = "DELETE FROM sessions;";
            PreparedStatement statement4 = (PreparedStatement) con.prepareStatement(sql_session);
            statement4.executeUpdate();

        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            // Close statements and connection in the 'finally' block to ensure proper cleanup
            try {
                if (statement != null) {
                    statement.close();
                }
                if (statement1 != null) {
                    statement1.close();
                }
                if (statement2 != null) {
                    statement2.close();
                }
                if (statement3 != null) {
                    statement3.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        
        xDisplay d = new xDisplay();
        d.displayTableImprot(table_Import_Procceed);
        d.displayTableImprot(table_Import);
        d.DisplayTotalImport(import_txtSumTotal, import_txtFinaleTotal);
        d.displayTableImprot(table_Import_Procceed);
