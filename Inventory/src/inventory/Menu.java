    private void import_txtCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_import_txtCategoryActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventory?autoReconnect=true&useSSL=false", "root", "");

            String cate = import_txtCategory.getSelectedItem().toString();

            String query;
            if ("All".equals(cate)) { // Compare strings using .equals() method
                query = "SELECT product, product_id FROM products;";
            } else {
                query = "SELECT p.product, p.product_id FROM products p JOIN category c ON p.category_id = c.category_id WHERE category_name = ?;";
            }

            PreparedStatement statement = (PreparedStatement) con.prepareStatement(query);

            if (!"All".equals(cate)) {
                statement.setString(1, cate);
            }

            ResultSet rs = statement.executeQuery();
            import_txtProduct.removeAllItems(); // Clear existing items
            while (rs.next()) {
                String productName = rs.getString("product");
                String id = rs.getString("product_id");
                import_txtProduct.addItem(productName);
                import_txtProduct_id.setText(id);

            }
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace(); // Print the stack trace for debugging
        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            try {
                if (con != null) {
                    con.close(); // Close the database connection in the finally block
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_import_txtCategoryActionPerformed

