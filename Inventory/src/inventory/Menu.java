    private void applyHoverEffect(java.awt.event.MouseEvent evt, JButton button) {
        button.setBackground(new Color(84,144,202));
        button.setForeground(Color.WHITE);
    }
    private void removeHoverEffect(java.awt.event.MouseEvent evt, JButton button) {
        button.setBackground(new Color(153,204,255));
        button.setForeground(new Color(69,69,69));
    }
    private void attachHoverListeners(Container container) {
    for (Component component : container.getComponents()) {
        if (component instanceof JButton) {
            JButton button = (JButton) component;
            button.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    applyHoverEffect(evt, button);
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    removeHoverEffect(evt, button);
                }
            });
        } else if (component instanceof Container) {
            attachHoverListeners((Container) component); // Recursively check nested containers
            }
        }
    
    }
